/**********************************************
 * @brief ：在指定位置显示一个字符
 * @param : Xpos: x起始坐标
 * @param : Ypos: y起始坐标
 * @param : acsii: 字符
 * @note  ：none
 * @retval: 绘制成功返回oc_OK，失败返回oc_ON
**********************************************/
void ocShowChar(uint16_t Xpos, uint16_t Ypos,char acsii)
{  	
	//根据OLED12864修改的
  uint8_t temp,t1,i;
	uint16_t x0 = Xpos,y0 = Ypos;
	
	acsii=acsii-' ';
										
	if(ocFontSet.fontForm == acsMin)
	{
		for(i = 0;i < acsMin;i++)
		{
			temp = asc1_0805[acsii][i];
			for(t1=0;t1<8;t1++)
			{
				if(temp&0x80)	
				{
						ocPutDot(x0,y0,ocFontSet.textColor);
				}  
				y0++;	
				temp<<=1;
			}
			
			x0++;
			y0 = Ypos;
		}
	}
	else if(ocFontSet.fontForm == acsMid)
	{
		for(i = 0;i < acsMid;i++)
		{
			temp = asc2_1210[acsii][0+i*2];
			for(t1=0;t1<8;t1++)
			{
				if(temp&0x80)	
				{
						ocPutDot(x0,y0,ocFontSet.textColor);
				}  
				y0++;	
				temp<<=1;
			}
			
			temp = asc2_1210[acsii][1+i*2];
			for(t1=0;t1<8;t1++)
			{
				if(temp&0x80)	
				{
						ocPutDot(x0,y0,ocFontSet.textColor);
				}  
				y0++;	
				temp<<=1;
			}
			
			x0++;
			y0 = Ypos;
		}
	}    	   	 	  
}   
 
/**********************************************
 * @brief ：显示字符串
 * @param : Xpos: x起始坐标
 * @param : Ypos: y起始坐标
 * @param : *p:字符串起始地址	
 * @note  ：none
 * @retval: 绘制成功返回oc_OK，失败返回oc_ON
**********************************************/
void ocShowString(uint16_t Xpos,uint16_t Ypos,char *Str)
{         
	while((*Str<='~')&&(*Str>=' '))//判断是不是非法字符!
	{       
		ocShowChar(Xpos,Ypos,*Str);
		Xpos+=ocFontSet.fontForm+1;
		Str++;
	}  
}

/**********************************************
 * @brief ：显示数值
 * @param : Xpos: x起始坐标
 * @param : Ypos: y起始坐标
 * @param : num:要显示的数值
 * @param : acc:精确到小数点后几位
 * @note  ：none
 * @retval: 绘制成功返回oc_OK，失败返回oc_ON
**********************************************/
void ocShowNum(uint16_t Xpos,uint16_t Ypos,float num,uint8_t acc)
{  
	uint16_t  a, b, aLen = 0, bLen = 0, buff;
	uint16_t  accData;
	uint8_t   acsii[10],i;		
	
	//计算精确度
	accData = pow(10, acc);
	
	//浮点数分离
	a=num;  //自动类型转换，取整数部分
	b=(int)(num*accData)%accData;  //乘10000后对10000取余，得到4位小数点后数字
	
	//整数部分提取
	buff = a;
	while(buff)
	{
		acsii[aLen] = buff%10;
		buff/=10;
		aLen ++;
	}
	
	//整数部分打印
	for(i = 0;i < aLen;i ++)
		ocShowChar(Xpos + (i*5),
	             Ypos,acsii[aLen - i - 1] + '0');
	
	//打印小数点
	if(b!=0)	
		ocShowChar(Xpos + (aLen*5),Ypos,'.');
	
	//小数部分提取
	buff = b;
	while(buff)
	{
		acsii[bLen] = buff%10;
		buff/=10;
		bLen ++;
	}
	
	//小数部分打印
	for(i = 0;i < bLen;i ++)
		ocShowChar(Xpos + 3 + ((aLen + i)*(5)),
	             Ypos,acsii[bLen - i - 1] + '0');
}

